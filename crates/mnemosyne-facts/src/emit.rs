// ────────────────────────────────────────────────────────────────────────────
// Protobuf emit (reference — wire format spec).
// ────────────────────────────────────────────────────────────────────────────

pub fn emit_protobuf(spec: &GraphSpec) -> String {
    let mut out = String::new();
    out.push_str("// Auto-generated by mnemosyne-facts (DESIGN §39 entity-indexer protobuf).\n");
    out.push_str("// Reference emit — wire format spec.\n\n");
    out.push_str("syntax = \"proto3\";\n\n");
    out.push_str("package mnemosyne.generated;\n\n");
    out.push_str("// KEY_LEN = 24 (composite key wire size)\n\n");

    for entity in &spec.entities {
        out.push_str(persistence_marker_rust(entity.persistence));
        out.push('\n');
        out.push_str(&format!("message {} {{\n", entity.name));
        let mut idx = 1u32;
        out.push_str(&format!(
            " uint64 {} = {}; // composite key — 8 B BE prefix\n",
            entity.key.branch_field, idx
        ));
        idx += 1;
        out.push_str(&format!(
            " uint64 {} = {}; // composite key — 8 B BE entity_id\n",
            entity.key.entity_field, idx
        ));
        idx += 1;
        out.push_str(&format!(
            " uint64 {} = {}; // composite key — 8 B BE valid_from\n",
            entity.key.valid_from_field, idx
        ));
        idx += 1;
        for field in &entity.fields {
            out.push_str(&format!(
                " {} {} = {};\n",
                proto_type_prefix(&field.ty, field.nullable),
                field.name,
                idx
            ));
            idx += 1;
        }
        out.push_str("}\n\n");
    }
    for relation in &spec.relations {
        out.push_str(persistence_marker_rust(relation.persistence));
        out.push('\n');
        out.push_str(&format!("message {} {{\n", relation.name));
        let mut idx = 1u32;
        out.push_str(&format!(
            " uint64 from = {}; // {} entity_id\n",
            idx, relation.from_entity
        ));
        idx += 1;
        out.push_str(&format!(
            " uint64 to = {}; // {} entity_id\n",
            idx, relation.to_entity
        ));
        idx += 1;
        for field in &relation.fields {
            out.push_str(&format!(
                " {} {} = {};\n",
                proto_type_prefix(&field.ty, field.nullable),
                field.name,
                idx
            ));
            idx += 1;
        }
        out.push_str("}\n\n");
    }
    out
}

fn proto_type_prefix(ty: &FieldType, nullable: bool) -> String {
    let base = match ty {
        FieldType::U64BigEndian => "uint64",
        FieldType::String => "string",
        FieldType::Bytes => "bytes",
        FieldType::EntityRef { .. } => "uint64",
        FieldType::NormalizedAssetRefs => "repeated uint64",
        FieldType::RowPerEncounter => "repeated uint64",
    };
    if nullable && !base.starts_with("repeated") {
        format!("optional {base}")
    } else {
        base.to_string()
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::canonical::{canonical_identifier_set, jaccard_inclusion, sha256_hex};
    use crate::fixture::design_doc_schema_fixture;

    #[test]
    fn five_languages_deterministic() {
        let spec = design_doc_schema_fixture();
        let a = emit_all_languages(&spec);
        let b = emit_all_languages(&spec);
        assert_eq!(a, b);
    }

    #[test]
    fn five_languages_distinct_sha256() {
        let spec = design_doc_schema_fixture();
        let m = emit_all_languages(&spec);
        let mut seen = std::collections::BTreeSet::new();
        for s in [&m.rust, &m.kotlin, &m.python, &m.cpp, &m.protobuf] {
            let h = sha256_hex(s);
            assert_eq!(h.len(), 64);
            assert!(seen.insert(h));
        }
        assert_eq!(seen.len(), 5);
    }

    #[test]
    fn cross_language_jaccard_one() {
        let spec = design_doc_schema_fixture();
        let canonical = canonical_identifier_set(&spec);
        let m = emit_all_languages(&spec);
        for (lang, text) in [
            ("rust", &m.rust),
            ("kotlin", &m.kotlin),
            ("python", &m.python),
            ("cpp", &m.cpp),
            ("protobuf", &m.protobuf),
        ] {
            let j = jaccard_inclusion(text, &canonical);
            assert!((j - 1.0).abs() < f64::EPSILON, "{lang}: {j}");
        }
    }

    #[test]
    fn rust_emit_contains_all_entities_and_key_codec() {
        let spec = design_doc_schema_fixture();
        let r = emit_rust(&spec);
        for n in ["Section", "ChangelogEntry", "FrozenList", "CrossRef"] {
            assert!(r.contains(&format!("pub struct {}", n)));
        }
        assert!(r.contains("pub const KEY_LEN: usize = 24;"));
        assert!(r.contains("BigEndian::write_u64(&mut buf[0..8]"));
        assert!(r.contains("BigEndian::write_u64(&mut buf[16..24]"));
    }

    #[test]
    fn protobuf_field_indices_start_at_one_and_increment() {
        let spec = design_doc_schema_fixture();
        let p = emit_protobuf(&spec);
        assert!(p.contains("uint64 branch_id = 1;"));
        assert!(p.contains("uint64 entity_id = 2;"));
        assert!(p.contains("uint64 valid_from = 3;"));
    }

    #[test]
    fn entity_ref_target_preserved_across_languages() {
        let spec = design_doc_schema_fixture();
        let m = emit_all_languages(&spec);
        // FrozenList.owner_section is EntityRef { target: "Section" }.
        assert!(m.rust.contains("/* ref: Section */"));
        assert!(m.kotlin.contains("/* ref: Section */"));
        assert!(m.python.contains("# ref: Section"));
        assert!(m.cpp.contains("/* ref: Section */"));
    }
}
