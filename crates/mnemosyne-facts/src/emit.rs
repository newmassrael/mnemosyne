//! 5-language emit — `GraphSpec` → Rust / Kotlin / Python / C++ / Protobuf source
//! strings. Deterministic byte-identical output for identical input.
//!
//! Prototype `bench/codegen-prototype/src/entity_indexer.rs::emit_*` production
//! previous. Rust authoritative, others reference (cross-language conformance source).

use crate::schema::{EntityDef, FieldType, GraphSpec, Persistence, RelationDef};

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct EmittedMultiLang {
    pub rust: String,
    pub kotlin: String,
    pub python: String,
    pub cpp: String,
    pub protobuf: String,
}

pub fn emit_all_languages(spec: &GraphSpec) -> EmittedMultiLang {
    EmittedMultiLang {
        rust: emit_rust(spec),
        kotlin: emit_kotlin(spec),
        python: emit_python(spec),
        cpp: emit_cpp(spec),
        protobuf: emit_protobuf(spec),
    }
}

// ────────────────────────────────────────────────────────────────────────────
// Rust emit (authoritative — production target compile path).
// ────────────────────────────────────────────────────────────────────────────

pub fn emit_rust(spec: &GraphSpec) -> String {
    let mut out = String::new();
    out.push_str("// Auto-generated by mnemosyne-facts (DESIGN §39 entity-indexer).\n");
    out.push_str("// Phase -1A stage 2C carry — 24 B BE composite key.\n\n");
    out.push_str("use byteorder::{BigEndian, ByteOrder};\n\n");
    out.push_str("pub const KEY_LEN: usize = 24;\n\n");

    for entity in &spec.entities {
        emit_rust_entity(&mut out, entity);
    }
    for relation in &spec.relations {
        emit_rust_relation(&mut out, relation);
    }
    out
}

fn emit_rust_entity(out: &mut String, entity: &EntityDef) {
    out.push_str(persistence_marker_rust(entity.persistence));
    out.push('\n');
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str(&format!("pub struct {} {{\n", entity.name));
    out.push_str(&format!(
        " pub {}: u64, // composite key — 8 B BE prefix\n",
        entity.key.branch_field
    ));
    out.push_str(&format!(
        " pub {}: u64, // composite key — 8 B BE entity_id\n",
        entity.key.entity_field
    ));
    out.push_str(&format!(
        " pub {}: u64, // composite key — 8 B BE valid_from\n",
        entity.key.valid_from_field
    ));
    for field in &entity.fields {
        out.push_str(&format!(
            " pub {}: {},\n",
            field.name,
            rust_type_for(&field.ty, field.nullable)
        ));
    }
    out.push_str("}\n");
    out.push_str(&format!("impl {} {{\n", entity.name));
    out.push_str(" pub fn encode_key(&self) -> [u8; KEY_LEN] {\n");
    out.push_str(" let mut buf = [0u8; KEY_LEN];\n");
    out.push_str(&format!(
        " BigEndian::write_u64(&mut buf[0..8], self.{});\n",
        entity.key.branch_field
    ));
    out.push_str(&format!(
        " BigEndian::write_u64(&mut buf[8..16], self.{});\n",
        entity.key.entity_field
    ));
    out.push_str(&format!(
        " BigEndian::write_u64(&mut buf[16..24], self.{});\n",
        entity.key.valid_from_field
    ));
    out.push_str(" buf\n");
    out.push_str(" }\n");
    out.push_str("}\n\n");
}

fn emit_rust_relation(out: &mut String, relation: &RelationDef) {
    out.push_str(persistence_marker_rust(relation.persistence));
    out.push('\n');
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str(&format!("pub struct {} {{\n", relation.name));
    out.push_str(&format!(
        " pub from: u64, // {} entity_id\n",
        relation.from_entity
    ));
    out.push_str(&format!(
        " pub to: u64, // {} entity_id\n",
        relation.to_entity
    ));
    for field in &relation.fields {
        out.push_str(&format!(
            " pub {}: {},\n",
            field.name,
            rust_type_for(&field.ty, field.nullable)
        ));
    }
    out.push_str("}\n\n");
}

fn persistence_marker_rust(p: Persistence) -> &'static str {
    match p {
        Persistence::Persistent => "// @persistent — explicit CF",
        Persistence::Derived => "// @derived — derived CF",
    }
}

fn rust_type_for(ty: &FieldType, nullable: bool) -> String {
    let base = match ty {
        FieldType::U64BigEndian => "u64".to_string(),
        FieldType::String => "String".to_string(),
        FieldType::Bytes => "Vec<u8>".to_string(),
        FieldType::EntityRef { target } => format!("/* ref: {target} */ u64"),
        FieldType::NormalizedAssetRefs => {
            "Vec<u64> // row-per-(asset, fact) normalized".to_string()
        }
        FieldType::RowPerEncounter => "Vec<u64> // row-per-encounter (§44)".to_string(),
    };
    if nullable {
        format!("Option<{base}>")
    } else {
        base
    }
}

